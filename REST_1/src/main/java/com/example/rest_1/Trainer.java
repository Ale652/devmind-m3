package com.example.rest_1;

public
class Trainer{
    private String name;
    private String zoomId;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getZoomId() {
        return zoomId;
    }

    public void setZoomId(String zoomId) {
        this.zoomId = zoomId;
    }

    @Override
    public String toString() {
        return "Trainer{" +
                "name='" + name + '\'' +
                ", zoomId='" + zoomId + '\'' +
                '}';
    }
}